#include <bits/stdc++.h>

using namespace std;

typedef unsigned int uint;
typedef unsigned long long ull;
typedef long long ll;

template <typename T>
void printarray(T v[], uint n) {
    for (uint i = 0; i < n; i ++) {
        cout << v[i] << " ";
    }
    cout << endl;
}

template <typename T>
void printvector(vector<T> v) {
    for (uint i = 0; i < v.size(); i ++) {
        cout << v[i] << " ";
    }
    cout << endl;
}

